const TRANSLATIONS: Record<LangCode, Dict> = { en, hi, te, kn };

function deviceDefault(): LangCode {
  try {
    const codes = Localization.getLocales().map(l => (l.languageCode || '').toLowerCase());
    const supported: LangCode[] = ['hi', 'te', 'kn'];
    const match = codes.find(c => supported.includes(c as LangCode));
    return (match as LangCode) || 'en';
  } catch {
    return 'en';
  }
}

// ── Context ─────────────────────────────────────────────────────────────────
interface I18nValue {
  lang: LangCode;
  setLang: (code: LangCode) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
}

const I18nContext = createContext<I18nValue | undefined>(undefined);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<LangCode>('en');

  // Load persisted language (or device default) on mount.
  useEffect(() => {
    (async () => {
      try {
        const saved = (await AsyncStorage.getItem(STORAGE_KEY)) as LangCode | null;
        if (saved && TRANSLATIONS[saved]) setLangState(saved);
        else setLangState(deviceDefault());
      } catch {
        setLangState('en');
      }
    })();
  }, []);

  const setLang = useCallback((code: LangCode) => {
    setLangState(code);
    AsyncStorage.setItem(STORAGE_KEY, code).catch(() => {});
  }, []);

  const t = useCallback(
    (key: string, vars?: Record<string, string | number>) => {
      const dict = TRANSLATIONS[lang] || en;
      let str = dict[key] ?? en[key] ?? key;
      if (vars) {
        for (const [k, v] of Object.entries(vars)) {
          str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
        }
      }
      return str;
    },
    [lang],
  );

  const value = useMemo<I18nValue>(() => ({ lang, setLang, t }), [lang, setLang, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const ctx = useContext(I18nContext);
  if (!ctx) {
    // Fallback so components still render if used outside the provider.
    return {
      lang: 'en',
      setLang: () => {},
      t: (key: string) => en[key] ?? key,
    };
  }
  return ctx;
}
